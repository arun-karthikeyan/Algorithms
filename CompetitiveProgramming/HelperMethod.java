import java.io.File;
import java.io.FileInputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStreamWriter;
import java.io.PrintWriter;
import java.util.ArrayList;

public class HelperMethod {
	private static int totalchars = 0, offset = 0;
	private static InputStream stream;
	private static byte[] buffer = new byte[1024];

	private static int readByte() {
		if (totalchars < 0)
			return 0;
		if (offset >= totalchars) {
			offset = 0;
			try {
				totalchars = stream.read(buffer);
			} catch (IOException e) {
				return 0;
			}
			if (totalchars <= 0)
				return -1;
		}
		return buffer[offset++];
	}

	private static int readInt() {
		int number = readByte();

		while (eolchar(number))
			number = readByte();

		int sign = 1;
		int val = 0;

		if (number == '-') {
			sign = -1;
			number = readByte();
		}

		do {
			if ((number < '0') || (number > '9'))
				return 0;
			val *= 10;
			val += (number - '0');
			number = readByte();
		} while (!eolchar(number));

		return sign * val;
	}

	private static long readLong() {
		int number = readByte();

		while (eolchar(number))
			number = readByte();

		int sign = 1;
		long val = 0;

		if (number == '-') {
			sign = -1;
			number = readByte();
		}

		do {
			if ((number < '0') || (number > '9')) {
				// return sign*val;
				return 0;
			}
			val *= 10;
			val += (number - '0');
			number = readByte();
		} while (!eolchar(number));

		return sign * val;
	}

	private static boolean eolchar(int c) {
		return c == ' ' || c == '\n' || c == -1 || c == '\r' || c == '\t';
	}

	private static final int W = 0, H = 1;

	static class State {
		int idx;
		int totalFlavour;
		int purchaseAmount;

		public State(int idx, int totalFlavour, int purchaseAmount) {
			this.idx = idx;
			this.totalFlavour = totalFlavour;
			this.purchaseAmount = purchaseAmount;
		}

		public boolean equals(Object c) {
			State obj = (State) c;
			return this.idx == obj.idx && this.totalFlavour == obj.totalFlavour
					&& this.purchaseAmount == obj.purchaseAmount;
		}

		public int hashCode() {
			return idx << 22 | totalFlavour << 15 | purchaseAmount;
		}
	}

	public static void main(String[] args) throws Exception {
		if (args.length > 0 && "fileip".equals(args[0])) {
			stream = new FileInputStream(new File("testip.txt"));
		} else {
			stream = System.in;
		}
		PrintWriter pw = new PrintWriter(new OutputStreamWriter(System.out));
		pw.println(Integer.toHexString(15));
		pw.flush();
		pw.close();
	}

}